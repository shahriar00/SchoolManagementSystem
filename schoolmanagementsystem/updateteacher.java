    private void submitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_submitActionPerformed
        // TODO add your handling code here:

        String tid= id.getText();
        String tn=name.getText();
        String spe= sp.getText();
        String dign=dg.getText();
        
        try{
            Class.forName("com.mysql.jdbc.Driver");
            Connection conn= DriverManager.getConnection("jdbc:mysql://localhost:3306/school", "root","");
            String sql= "update `teacherdata` SET `Name`='"+tn+"',`Specialize`='"+spe+"',`Designation`='"+dign+"'WHERE ID='"+tid+"'";
            PreparedStatement ptst=conn.prepareStatement(sql);
            ptst.execute();
            
            JOptionPane.showMessageDialog(null, "Data Updated Successfully.");
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_submitActionPerformed

